    public void GantiPassword(String baru,String current){
        query="UPDATE `"+tipe+"` SET `pass`='"+baru+"' WHERE `"+tipe+"`.`username`='"+username+"' AND `"+tipe+"`.`pass`='"+current+"' ";
        System.out.println(query);
        try{
            PreparedStatement stat=(PreparedStatement) konek.prepareStatement(query);
            stat.executeUpdate();
            JOptionPane.showMessageDialog(rootPane, "Password Berhasil Diganti");
            this.dispose();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(rootPane, "Password yang anda masukkan salah, Password belum diganti");
        }
    }
    
    
